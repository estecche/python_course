"""
These are examples of doing a casting.
"""
number = 45;
# Here we need to cast the number to a string otherwise it won't work the concatenation
print("The number is = " + str(number))
# We can also cast something else taking into consideration that it should make sense
number_string = "28787234"
print(int(number_string))
number_float = "12.34"
print(float(number_float))
is_valid = True
print(is_valid)
print(str(is_valid))